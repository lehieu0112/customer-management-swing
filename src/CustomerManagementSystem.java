    private void showCustomer(Customers c){
        String customerID = Integer.toString(c.getCustomerID());
        customerIDTextField.setText(customerID);
        customerNameTextField.setText(c.getCustomerName());
        dateOfJoinTextField.setText(c.getDateOfJoin());
        String revenue = Double.toString(c.getRevenue());
        revenueTextField.setText(revenue);
    }
    private void clearFields(){
        customerIDTextField.setText("");
        customerNameTextField.setText("");
        dateOfJoinTextField.setText("");
        revenueTextField.setText("");
    }
    private boolean isValidData(){
        SwingValidator sv = new SwingValidator();
        if(!sv.isPresent(this.customerIDTextField, "CustomerID"))
            return false;       
        if(!sv.isInterger(this.customerIDTextField , "CustomerID"))
            return false;
        if(!sv.isPresent(this.customerNameTextField , "CustomerName")) 
            return false;
        if(!sv.isPresent(this.dateOfJoinTextField , "Date of Join")) 
            return false;
        if(!sv.isPresent(this.revenueTextField , "Revenue")) 
            return false;
        if(!sv.isDouble(this.revenueTextField , "Revenue")) 
            return false;
        return true;
    }
    private void displayMode(boolean e){
        customerIDTextField.setEditable(!e);
        customerNameTextField.setEditable(!e);
        dateOfJoinTextField.setEditable(!e);
        revenueTextField.setEditable(!e);
        addButton.setEnabled(e);
        updateButton.setEnabled(e);
        deleteButton.setEnabled(e);
    }
    private Customers getCustomer(){
        Customers newCustomer = new Customers();        
        String customerID = customerIDTextField.getText();
        int id = Integer.parseInt(customerID);
        newCustomer.setCustomerID(id);
        String customerName = customerNameTextField.getText();
        newCustomer.setCustomerName(customerName);
        String dateOfJoin = dateOfJoinTextField.getText();
        newCustomer.setDateOfJoin(dateOfJoin);
        String revenue = revenueTextField.getText();
        double rv = Double.parseDouble(revenue);
        newCustomer.setRevenue(rv);
        return newCustomer;
    }
    
