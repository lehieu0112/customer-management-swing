    private void acceptButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acceptButtonActionPerformed
        // TODO add your handling code here:
        if(isValidData()){
            if(mode.equalsIgnoreCase("add")){
                if(cDB.addCustomer(getCustomer())){               
                    JOptionPane.showMessageDialog(acceptButton, "Add successful","Information",JOptionPane.INFORMATION_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(acceptButton, "Add failed","Information",JOptionPane.INFORMATION_MESSAGE);
                }
            }else if(mode.equalsIgnoreCase("update")){
                if(cDB.updateCustomer(getCustomer(), oldID)){
                    JOptionPane.showMessageDialog(acceptButton, "Update successful","Information",JOptionPane.INFORMATION_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(acceptButton, "Update failed","Information",JOptionPane.INFORMATION_MESSAGE);
                }
            }
        }
        displayMode(true);
        showCustomer(cDB.getFirstCustomer());
        mode = "";
    }//GEN-LAST:event_acceptButtonActionPerformed

